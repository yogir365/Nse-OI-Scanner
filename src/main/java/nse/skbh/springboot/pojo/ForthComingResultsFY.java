package nse.skbh.springboot.pojo;

public class ForthComingResultsFY {

	private String symbol;
	private String company;
	private String purpose;
	private String boardMeetingDate;
	
	public String getSymbol() {
		return symbol;
	}
	public void setSymbol(String symbol) {
		this.symbol = symbol;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getPurpose() {
		return purpose;
	}
	public void setPurpose(String purpose) {
		this.purpose = purpose;
	}
	public String getBoardMeetingDate() {
		return boardMeetingDate;
	}
	public void setBoardMeetingDate(String boardMeetingDate) {
		this.boardMeetingDate = boardMeetingDate;
	}
	
	
}
