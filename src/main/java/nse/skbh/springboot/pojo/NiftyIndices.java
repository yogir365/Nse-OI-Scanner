package nse.skbh.springboot.pojo;

public class NiftyIndices {
	
	
	private String timeVal;
	private String indexName;
	private String previousClose;
	private String open;
	private String high;
	private String low;
	private String last;
	private String percChange;
	private String yearHigh;
	private String yearLow;
	private String indexOrder;
	public String getTimeVal() {
		return timeVal;
	}
	public void setTimeVal(String timeVal) {
		this.timeVal = timeVal;
	}
	public String getIndexName() {
		return indexName;
	}
	public void setIndexName(String indexName) {
		this.indexName = indexName;
	}
	public String getPreviousClose() {
		return previousClose;
	}
	public void setPreviousClose(String previousClose) {
		this.previousClose = previousClose;
	}
	public String getOpen() {
		return open;
	}
	public void setOpen(String open) {
		this.open = open;
	}
	public String getHigh() {
		return high;
	}
	public void setHigh(String high) {
		this.high = high;
	}
	public String getLow() {
		return low;
	}
	public void setLow(String low) {
		this.low = low;
	}
	public String getLast() {
		return last;
	}
	public void setLast(String last) {
		this.last = last;
	}
	public String getPercChange() {
		return percChange;
	}
	public void setPercChange(String percChange) {
		this.percChange = percChange;
	}
	public String getYearHigh() {
		return yearHigh;
	}
	public void setYearHigh(String yearHigh) {
		this.yearHigh = yearHigh;
	}
	public String getYearLow() {
		return yearLow;
	}
	public void setYearLow(String yearLow) {
		this.yearLow = yearLow;
	}
	public String getIndexOrder() {
		return indexOrder;
	}
	public void setIndexOrder(String indexOrder) {
		this.indexOrder = indexOrder;
	}
	
	

}
